function  hide_quangcao_right() {
	// an div  tat quan cao
	document.getElementById('eixt_quangcao_right').style.display="none";
	// an image quang cao
	document.getElementById('content_quangcao_right').style.display="none";
	// hien dic xem quang cao
	document.getElementById('look_quangcao_right').style.display="block";
}

